class UserDetails {
  String name;
  double height;
  double weight;
  String gender;
  int age;
  String email;

  UserDetails(
      String name, double height, double weight, String gender, int age,String email) {
    this.name = name;
    this.height = height;
    this.weight = weight;
    this.gender = gender;
    this.age = age;
    this.email = email;
  }
}

class UserName {
  String name;

  UserName(
      String name) {
    this.name = name;
  }
}
